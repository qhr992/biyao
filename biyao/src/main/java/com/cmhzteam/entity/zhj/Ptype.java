package com.cmhzteam.entity.zhj;

import java.util.List;

public class Ptype {
    private int pid;
    private String ptype_name;
    private List<Stype> slist;
    public Ptype() {
    }

    public Ptype(int pid, String ptype_name, List<Stype> slist) {
        this.pid = pid;
        this.ptype_name = ptype_name;
        this.slist = slist;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getPtype_name() {
        return ptype_name;
    }

    public void setPtype_name(String ptype_name) {
        this.ptype_name = ptype_name;
    }

    public List<Stype> getSlist() {
        return slist;
    }

    public void setSlist(List<Stype> slist) {
        this.slist = slist;
    }

    @Override
    public String toString() {
        return "Ptype{" +
                "pid=" + pid +
                ", ptype_name='" + ptype_name + '\'' +
                ", slist=" + slist +
                '}';
    }
}
