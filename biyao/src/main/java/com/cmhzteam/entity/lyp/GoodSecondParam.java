package com.cmhzteam.entity.lyp;

/**
 * 商品详情从属性表
 */
public class GoodSecondParam {
    private int gspid;//从属性id
    private String sparam_name;//从属性名称
    private double add_prices;//加价
    private int stocks;//库存
    private int gmpid;//主属性id

    public GoodSecondParam() {
    }

    public GoodSecondParam(int gspid, String sparam_name, double add_prices, int stocks, int gmpid) {
        this.gspid = gspid;
        this.sparam_name = sparam_name;
        this.add_prices = add_prices;
        this.stocks = stocks;
        this.gmpid = gmpid;
    }

    public int getGmpid() {
        return gmpid;
    }

    public void setGmpid(int gmpid) {
        this.gmpid = gmpid;
    }

    public int getGspid() {
        return gspid;
    }

    public void setGspid(int gspid) {
        this.gspid = gspid;
    }

    public String getSparam_name() {
        return sparam_name;
    }

    public void setSparam_name(String sparam_name) {
        this.sparam_name = sparam_name;
    }

    public double getAdd_prices() {
        return add_prices;
    }

    public void setAdd_prices(double add_prices) {
        this.add_prices = add_prices;
    }

    public int getStocks() {
        return stocks;
    }

    public void setStocks(int stocks) {
        this.stocks = stocks;
    }

    @Override
    public String toString() {
        return "GoodSecondParam{" +
                "gspid=" + gspid +
                ", sparam_name='" + sparam_name + '\'' +
                ", add_prices=" + add_prices +
                ", stocks=" + stocks +
                ", gmpid=" + gmpid +
                '}';
    }
}
